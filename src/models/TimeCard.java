package models;
import java.util.Calendar;
import java.util.Date;

public class TimeCard {
	private String date;
	private double hours;
	
    public TimeCard() {
    	this.date = "";
    	this.hours = hours;
    }

    public String getDate() {
        return date;
    }

    public double getHours() {
        return hours;
    }
}